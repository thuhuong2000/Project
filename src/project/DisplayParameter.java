package project;

public class DisplayParameter {
	public int id;
	public String name;
	public int classID;
	public String birthday;
	public String address;
	public String phone;

	public DisplayParameter(int id, String name, int classID, String birthday, String address, String phone) {
		this.id = id;
		this.name = name;
		this.classID = classID;
		this.birthday = birthday;
		this.address = address;
		this.phone = phone;
	}
}